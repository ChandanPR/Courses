package basics;

public class Bits {
	
	public static void main(String[] args) {
		System.out.println(32 >> 1);//divide by 2
		System.out.println(32 >> 3);//divide by 2pow3
		System.out.println(32<<1);//multiply by 2
		System.out.println(32<<3);//multiply by 2pow3
		System.out.println(32>>>1);//unsigned right shift
	}

}
